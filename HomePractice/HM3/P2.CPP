// Relational Opearator Comparsion

#include <iostream>
using namespace std;

int main()
{

    int a = 35, b = 20;
    cout << "a>b: " << (a > b) << endl;
    cout << "a<b: " << (a < b) << endl;
    cout << "a!b: " << (a != b) << endl;
    cout << "a==b: " << (a == b) << endl;
    cout << "a<=b: " << (a <= b) << endl;
    cout << "a>=b: " << (a >= b) << endl;

    return 0;
}