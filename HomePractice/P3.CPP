// Cpp Program for Multiplying the numbers

#include <iostream>
using namespace std;

int main()
{

    // Declaring the variables
    int A, B, C;

    // Giving the values to the Variables

    A = 34;
    B = 12;
    C = 45;

    // Result of the Multiplication to be shown

    int Result = A * B * C;
    cout << "The Final Result is:" << Result << endl;
    return 0;
}