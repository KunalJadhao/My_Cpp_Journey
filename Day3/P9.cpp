// Size of Operator
#include <iostream>
using namespace std;

int main()
{
    int a;
    double b;
    char c;
    bool isstudent = true;
    cout << "Size of int: " << sizeof(a) << " bytes" << endl;
    cout << "Size of double: " << sizeof(b) << " bytes" << endl;
    cout << "Size of Char: " << sizeof(c) << " bytes" << endl;
    cout << "Size of bool: " << sizeof(isstudent) << " bytes" << endl;
    return 0;
}
